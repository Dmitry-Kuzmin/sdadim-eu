<h2 id="faq">Частые вопросы (FAQ)</h2>

<h3>Нужен ли нотариальный перевод российских прав?</h3>
<p>Для сдачи экзамена — нет. Ваши иностранные права просто изымаются при получении испанских (или хранятся у вас, поскольку Испания их не передаёт в страну выдачи). Важен ваш TIE и медсправка.</p>

<h3>Можно ли самостоятельно записаться на экзамен без автошколы?</h3>
<p>Да — для теоретического экзамена автошкола не нужна. Вы сами оплачиваете пошлину и записываетесь через <code>sede.dgt.gob.es</code>. Для практического экзамена автошкола обязательна (они записывают от своего имени).</p>

<h3>Сколько времени занимает весь процесс?</h3>
<p>В среднем 3–6 месяцев: 1–2 месяца на подготовку к теории, 1–2 месяца на практику, ещё 1–2 месяца ожидания дат экзаменов. В популярных городах сроки длиннее — экзаменационные слоты разбирают быстро.</p>

<h3>Сложно ли сдать теорию, если испанский на уровне A2?</h3>
<p>Реально, но нужна интенсивная практика тестов. Многие вопросы стандартизированы, и при наработке через симуляторы уровень языка уходит на второй план. Тем не менее, несколько слов из ПДД стоит выучить отдельно (ширина дороги, приоритет, знаки).</p>

<h3>Что такое знак «L» и сколько его носить?</h3>
<p>Знак «L» (learner — новичок) — жёлтый прямоугольник с красной «L» — устанавливается на заднее стекло. Обязателен <strong>первый год</strong> после получения прав. С ним действует ограничение скорости 100 км/ч на автострадах (вместо 120 км/ч) и норма алкоголя 0,1 промилле (против 0,5 для опытных).</p>

<h3>Что если я провалю теорию дважды?</h3>
<p>При двух неудачах в рамках одной пошлины (осталась одна попытка на практику) нужно повторно оплатить пошлину (~95 €). Срок действия результата теории — 2 года.</p>

