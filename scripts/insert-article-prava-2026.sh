<h2 id="kto-obyazan">Кто обязан получить права в Испании</h2>

<p>Ваш статус в стране напрямую определяет, нужны ли вам испанские права.</p>

<h3>Туристы и студенты</h3>
<p>Если вы находитесь в Испании по туристической визе или как студент — вы можете ездить на правах своей страны при условии, что она подписала Венскую конвенцию о дорожном движении. Россия, Беларусь, Казахстан и Украина — в списке. Для стран вне конвенции потребуется международное водительское удостоверение (МВУ), которое оформляется на родине.</p>

<h3>Резиденты (ВНЖ/ПМЖ)</h3>
<p><strong>После 6 месяцев с момента получения карты резидента (TIE) вы обязаны перейти на испанские права.</strong> После этого срока вождение с иностранным удостоверением грозит штрафом — даже при большом стаже. Инспекторы DGT имеют доступ к базе данных резидентов и легко выявят нарушение.</p>

